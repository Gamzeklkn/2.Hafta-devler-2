package oop1;

public class Customer {
	private int id;
	private String customerNamber;
	private String phone;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCustomerNamber() {
		return customerNamber;
	}

	public void setCustomerNamber(String customerNamber) {
		this.customerNamber = customerNamber;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

}
